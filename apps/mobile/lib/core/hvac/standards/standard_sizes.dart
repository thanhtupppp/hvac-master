  static double findNearestStandardRound(
    double rawDiameter,
    List<double> standardList,
  ) {
    if (standardList.isEmpty) return rawDiameter;
    return standardList.reduce(
      (a, b) => (a - rawDiameter).abs() < (b - rawDiameter).abs() ? a : b,
    );
